console.log("Arithmetic Operators: ");
var a=10;
var b=5;

console.log(`a = ${a} b = ${b}`);

console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a x b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);



console.log("Comparison Operators: ");
var x = 10;
var y = 20;

console.log(`x = ${x} y = ${y}`);

console.log("x > y : ", x > y);
console.log("x < y : ", x < y);
console.log("x >= y : ", x >= y);
console.log("x <= y : ", x <=y );
console.log("x == y : ", x == y);
console.log("x != y : ", x != y);


console.log("Logical Operator: ");
var i = 10;
var j = 20;
var k = 30;

console.log(`i = ${i} j = ${j} k = ${k}`);

console.log("i<j && j<k : ", i<j && j<k);
console.log("i<j && j>k : ", i<j && j>k);
console.log("i>j && j<k : ", i>j && j<k);
console.log("i>j && j>k : ", i>j && j>k);

console.log("i<j || j<k : ", i<j || j<k);
console.log("i<j || j>k : ", i<j || j>k);
console.log("i>j || j<k : ", i>j || j<k);
console.log("i>j || j>k : ", i>j || j>k);

console.log("!(i<j) : ", !(i<j));
console.log("!(i>j) : ", !(i>j));

